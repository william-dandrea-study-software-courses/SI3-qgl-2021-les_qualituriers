package fr.unice.polytech.si3.qgl.qualituriers.utils.helpers;

public class MyColumn2DMat {
    private final double a, b;

    public MyColumn2DMat(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
